using System;

namespace Area_do_Circulo
{
    internal class Program
    {
        static void Main(string[] args)
        {
            raio = float(input())
            area = round(3.14159 * raio * *2, 4)
            area = '{:6.4f}'.format(area)
            print(f"A={area}")
        }
    }
}
